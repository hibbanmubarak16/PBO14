    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            EntityManager em = emf.createEntityManager();
            em.getTransaction().begin();
            
                TypedQuery<KoleksiBuku> query = em.createQuery(
                "SELECT k FROM KoleksiBuku k WHERE k.idBuku = :idBuku AND k.judul = :judul "
                + "AND k.genre = :genre AND k.pengarang = :pengarang AND k.tahunTerbit = :tahunTerbit" , KoleksiBuku.class);

                query.setParameter("idBuku", IdbukuLama);
                query.setParameter("judul", JudulLama);
                query.setParameter("genre", GenreLama);
                query.setParameter("pengarang", PengarangLama);
                query.setParameter("tahunTerbit", TahunterbitLama);
                
                
                List<KoleksiBuku> hasil = query.getResultList();
                
                if (!hasil.isEmpty()) {
                KoleksiBuku kb = hasil.get(0);
                
                em.remove(kb);
                em.getTransaction().commit();
                    JOptionPane.showMessageDialog(this, "Data berhasil dihapus!");
                    dispose();
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Error: data tidak ditemukan " + e.getMessage());
            }   // TODO add your handling code here:
    }//GEN-LAST:event_jButton1ActionPerformed

