    private void jButton7ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton7ActionPerformed
        String pw = new String(txtPw.getPassword()).trim();
        String pw2 = new String(txtPw2.getPassword()).trim();
        char[] p = txtPw.getPassword();
        char[] p2 = txtPw2.getPassword();

        if (pw.equals("")) {
            JOptionPane.showMessageDialog(this, "Isi Semua Data!",
                "Peringatan", JOptionPane.WARNING_MESSAGE);
        }  else if (pw.length() < 8) {
            JOptionPane.showMessageDialog(this,
                "Password minimal 8 karakter!",
                "Peringatan", JOptionPane.WARNING_MESSAGE);
            return;
        }  else if (pw2.equals("")) {
            JOptionPane.showMessageDialog(this, "Isi Semua Data!",
                "Peringatan", JOptionPane.WARNING_MESSAGE);
        }  else if (!Arrays.equals(p, p2)) {
            JOptionPane.showMessageDialog(this,
                "Password dan Konfirmasi Password tidak sama!",
                "Peringatan", JOptionPane.WARNING_MESSAGE);
            return;
        } else {
            try {
                EntityManager em = emf.createEntityManager();
                em.getTransaction().begin();

                Akun a = em.find(Akun.class, txtUser.getText());
                a.setPassword(txtPw.getText());

                em.merge(a);
                em.getTransaction().commit();

                JOptionPane.showMessageDialog(this, "Password berhasil diubah!");
                dispose();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this,
                    "Terjadi kesalahan koneksi: " + e.getMessage(),
                    "Error", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_jButton7ActionPerformed

