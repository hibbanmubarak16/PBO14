    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if (txtUser.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Isi Username!",
                "Peringatan", JOptionPane.WARNING_MESSAGE);
        } else {
            try {

                EntityManager em = emf.createEntityManager();
                em.getTransaction().begin();

                Akun a = em.find(Akun.class, txtUser.getText());

                if (a != null) {
                    String inputIbu = JOptionPane.showInputDialog(
                        this,
                        "Username ditemukan! "
                        + "Masukkan Kode Rahasia Dirimu untuk konfirmasi:",
                        "Konfirmasi Keamanan",
                        JOptionPane.QUESTION_MESSAGE
                    );

                    if (inputIbu == null) {
                        return;
                    }

                    if (!inputIbu.equalsIgnoreCase(a.getNamaIbu())) {
                        JOptionPane.showMessageDialog(this,
                            "Kode Rahasia tidak sesuai!",
                            "Peringatan", JOptionPane.WARNING_MESSAGE);
                        return;
                    }

                    JOptionPane.showMessageDialog(this,
                        "Verifikasi berhasil! Silahkan ubah password.",
                        "Informasi", JOptionPane.INFORMATION_MESSAGE);

                    jPanel2.setVisible(true);
                    txtPw.requestFocus();
                } else {
                    JOptionPane.showMessageDialog(this,
                        "Username tidak ditemukan!",
                        "Error", JOptionPane.ERROR_MESSAGE);
                }

                em.getTransaction().commit();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this,
                    "Terjadi kesalahan koneksi: " + e.getMessage(),
                    "Error", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

